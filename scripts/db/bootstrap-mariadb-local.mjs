import path from "path";
import { fileURLToPath } from "url";
import dotenv from "dotenv";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../..");

dotenv.config({ path: path.join(repoRoot, ".env.local") });
dotenv.config({ path: path.join(repoRoot, ".env") });

/** Lê variável de ambiente ou fallback; lança se ausente. */
function required(name, fallback) {
  const value = process.env[name] ?? fallback;
  if (!value) {
    throw new Error(`Missing ${name} in .env.local`);
  }
  return value;
}

/** Escapa valor para literal SQL entre aspas simples. */
function sqlLiteral(value) {
  return `'${String(value).replace(/\\/g, "\\\\").replace(/'/g, "''")}'`;
}

/** Gera SQL de bootstrap do banco claroEmpresarial na stdout. */
function main() {
  const user = required("SIR_DB_USER", process.env.HFC_DB_USER);
  const password = required("SIR_DB_PASSWORD", process.env.HFC_DB_PASSWORD);
  const database = required("SIR_DB_NAME", "claroEmpresarial");

  const sql = `-- Generated by scripts/db/bootstrap-mariadb-local.mjs
-- Pipe into MariaDB as admin:
--   node scripts/db/bootstrap-mariadb-local.mjs | sudo mariadb

CREATE DATABASE IF NOT EXISTS ${quoteId(database)}
  CHARACTER SET utf8mb4
  COLLATE utf8mb4_general_ci;

CREATE USER IF NOT EXISTS ${sqlLiteral(user)}@'localhost' IDENTIFIED BY ${sqlLiteral(password)};
CREATE USER IF NOT EXISTS ${sqlLiteral(user)}@'127.0.0.1' IDENTIFIED BY ${sqlLiteral(password)};

GRANT ALL PRIVILEGES ON ${quoteId(database)}.* TO ${sqlLiteral(user)}@'localhost';
GRANT ALL PRIVILEGES ON ${quoteId(database)}.* TO ${sqlLiteral(user)}@'127.0.0.1';

FLUSH PRIVILEGES;
`;

  process.stdout.write(sql);
}

/** Escapa identificador MySQL com backticks. */
function quoteId(name) {
  return `\`${String(name).replace(/`/g, "``")}\``;
}

main();
